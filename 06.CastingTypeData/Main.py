# Casting --- merubah dari dari satu tipe data ke tipe data yang lain

# int
valueA = 200

valueFloat = float(valueA)
valueString = str(valueA)
valueBool = bool(valueA)

print("Data = ", valueA, "Bertipe = ", type(valueA))
print("Data = ", valueFloat, "Bertipe = ", type(valueFloat))
print("Data = ", valueString, "Bertipe = ", type(valueString))
print("Data = ", valueBool, "Bertipe = ", type(valueBool))